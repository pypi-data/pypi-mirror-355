reverse_map = {v: k for k, v in char_map.items()}

def encrypt(text):
    return " ".join([char_map.get(ch, f"[{ch}]") for ch in text])

def decrypt(code):
    return "".join([reverse_map.get(part, f"[{part}]") for part in code.strip().split()])
